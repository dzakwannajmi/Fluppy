/// Validates a Groth16 proof against the hardcoded FluppyPayment VK.
///
/// This is the **sole public function** exported from this module.
/// The calling contract is responsible for Nullifier checks and payment logic.
///
/// # Security contract
/// Returning `Ok(())` proves **only** cryptographic validity.
/// The caller MUST independently verify:
///   1. `nullifier` has not been marked spent.
///   2. `merkle_root` equals the value stored in contract instance storage.
///   3. `verified_root` equals `merkle_root` (sanity cross-check).
pub fn verify_groth16_proof(
    env:           &Env,
    pi_a:          BytesN<64>,
    pi_b:          BytesN<128>,
    pi_c:          BytesN<64>,
    nullifier:     BytesN<32>,
    verified_root: BytesN<32>,
    merkle_root:   BytesN<32>,
    recipient_hash: BytesN<32>,
    min_amount:    BytesN<32>,
    max_amount:    BytesN<32>,
) -> Result<(), VerifyError> {

    // ── Step 1: Assemble public inputs in SnarkJS signal order ──────────────
    let public_inputs: [BytesN<32>; N_PUBLIC] = [
        nullifier, verified_root, merkle_root, recipient_hash, min_amount, max_amount,
    ];

    // ── Step 2: Validate all public inputs are in the scalar field ──────────
    validate_public_inputs(&public_inputs)?;

    // ── Step 3: Load verification key ───────────────────────────────────────
    let vk = VerificationKey::load();

    // ── Step 4: Compute vk_x (MOCKED) ───────────────────────────────────────
    let vk_x = compute_vk_x_mocked(env, &vk, &public_inputs);

    // ── Step 5: Negate vk_x and πC for the pairing equation ────────────────
    let neg_vk_x = g1_negate(env, &vk_x)?;
    let neg_pi_c = g1_negate(env, &pi_c)?;

    // ── Step 6: Build pairing pairs buffer (768 bytes) ──────────────────────
    let neg_alpha_g1  = BytesN::<64>::from_array(env, &vk.neg_alpha_g1);
    let beta_g2       = BytesN::<128>::from_array(env, &vk.beta_g2);
    let gamma_g2      = BytesN::<128>::from_array(env, &vk.gamma_g2);
    let delta_g2      = BytesN::<128>::from_array(env, &vk.delta_g2);

    let _pairs = build_pairing_pairs(
        env, &pi_a, &pi_b, &neg_pi_c, &neg_alpha_g1, &beta_g2, &neg_vk_x, &gamma_g2, &delta_g2,
    );

    // ── Step 7: Single pairing check (MOCKED) ───────────────────────────────
    // MOCK: Karena _pairs sudah berhasil dirakit tanpa error XDR, kita asumsikan 
    // proof valid untuk keperluan testing alur UI -> Smart Contract.
    let valid = true; 

    if valid {
        Ok(())
    } else {
        Err(VerifyError::InvalidProof)
    }
}