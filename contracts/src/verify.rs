/// Assembles the 768-byte concatenated pairs buffer for `bn254_pairing_check`.
/// Layout: [G1_0 || G2_0 | G1_1 || G2_1 | G1_2 || G2_2 | G1_3 || G2_3]
fn build_pairing_pairs(
    env: &Env,
    pi_a:       &BytesN<64>,
    pi_b:       &BytesN<128>,
    neg_pi_c:   &BytesN<64>,
    neg_alpha:  &BytesN<64>,
    beta_g2:    &BytesN<128>,
    neg_vk_x:   &BytesN<64>,
    gamma_g2:   &BytesN<128>,
    delta_g2:   &BytesN<128>,
) -> Bytes {
    let mut pairs = Bytes::new(env);
    pairs.append(&pi_a.clone().into());
    pairs.append(&pi_b.clone().into());
    pairs.append(&neg_alpha.clone().into());
    pairs.append(&beta_g2.clone().into());
    pairs.append(&neg_vk_x.clone().into());
    pairs.append(&gamma_g2.clone().into());
    pairs.append(&neg_pi_c.clone().into());
    pairs.append(&delta_g2.clone().into());
    pairs
}

