//! Pure cryptographic verification for the FluppyPayment Circom circuit.
//! **No payment logic lives here.** This module is a narrow verification
//! gateway — the only thing it does is accept or reject a Groth16 proof.
//!
//! ## Circuit identity
//! ```circom
//! component main {
//!     public [merkleRoot, recipientHash, minAmount, maxAmount]
//! } = FluppyPayment(20);
//! // outputs: nullifier, verifiedRoot
//! ```
//!
//! ## Public signal ordering (MUST match SnarkJS export exactly)
//! SnarkJS lists *outputs* before *declared public inputs*:
//! | vk_ic index | Signal        | Kind           |
//! |-------------|---------------|----------------|
//! | IC[1]       | nullifier     | circuit output |
//! | IC[2]       | verifiedRoot  | circuit output |
//! | IC[3]       | merkleRoot    | public input   |
//! | IC[4]       | recipientHash | public input   |
//! | IC[5]       | minAmount     | public input   |
//! | IC[6]       | maxAmount     | public input   |
//!
//! ## Groth16 verification equation
//! ```text
//! e(πA, πB) · e(−α, β) · e(−vk_x, γ) · e(−πC, δ) = 1_GT
//! vk_x = IC[0] + Σ_{i=1}^{6} ( public_input[i-1] · IC[i] )
//! ```
//!
//! ## Encoding conventions (big-endian throughout)
//! - **G1 point** → 64 bytes  : `x[0..32] || y[0..32]`
//! - **G2 point** → 128 bytes : `x.c1[0..32] || x.c0[0..32] || y.c1[0..32] || y.c0[0..32]`
//! - **Scalar** → 32 bytes  : standard big-endian field element
//!
//! ─────────────────────────────────────────────────────────────────────────────
//! ⚠  PROTOCOL STATUS — READ BEFORE DEPLOYING TO MAINNET
//! ─────────────────────────────────────────────────────────────────────────────
//! BN254 host functions (`bn254_g1_add`, `bn254_g1_mul`, `bn254_pairing_check`)
